void addICbuff(uint32_t * IC, uint32_t * ARR, uint32_t TIM_CHANNEL, uint32_t * AVER) {
   if (* IC >= c_ARR)
   {* IC = 0;
    READY =1;
   }
  ARR[* IC] =    HAL_TIM_ReadCapturedValue(phtim_ZC, TIM_CHANNEL);
    (* IC)++;
  * AVER = 0; 
  uint32_t i;
  for (i= 0; i< c_ARR; i++)
    (* AVER) += ARR[i];
  (* AVER) /=c_ARR;
}
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
  if (htim == phtim_ZC) {
  if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) {
    addICbuff(&IC1, &IC1ARR[0],TIM_CHANNEL_1,&AVER1); 
    if (READY == 1)
   htim->Instance->CCR4 = AVER1 /2 +OFFSET;  
  //    htim1.Instance->CCR4 = OFFSET; 
  }
  if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2) {
    addICbuff(&IC2, &IC2ARR[0],TIM_CHANNEL_2,&AVER2); 
    if (READY == 1)    
    htim->Instance->ARR = AVER2 / 2;  
  }
    freq2 = (float) 72000000/ (float) 13 / (float)AVER2;
  }

}
